import turtle as t

def house(x,y):
    t.penup()
    t.goto(x,y)
    t.setheading(0)
    t.pendown()
    t.fillcolor('orange')
    t.begin_fill()
    t.forward(60)
    t.left(90)
    t.forward(60)
    t.left(90)
    t.forward(60)
    t.left(90)
    t.forward(60)
    t.end_fill()
    t.fillcolor('brown')
    t.begin_fill()
    t.left(90)
    t.forward(15)
    t.left(90)
    t.forward(40)
    t.right(90)
    t.forward(35)
    t.right(90)
    t.forward(40)
    t.end_fill()
    t.penup()
    t.goto((x+60),(y+60))
    t.pendown()
    t.fillcolor('blue')
    t.begin_fill()
    t.left(180)
    t.forward(20)
    t.left(90)
    t.forward(10)
    t.left(90)
    t.forward(20)
    t.end_fill()
    t.fillcolor('gray')
    t.begin_fill()
    t.goto(x,(y+60))
    t.goto((x+30),(y+80))
    t.goto((x+60),(y+60))
    t.penup()
    t.end_fill()
    t.fillcolor('yellow')
    t.begin_fill()
    t.setheading(0)
    t.goto((x+30),(y+70))
    t.pendown()
    for _ in range(5):
        t.forward(10)
        t.right(144)
        t.forward(5)
        t.left(72)
    t.end_fill()

t.setup(1000,500)
t.penup()
t.goto(-500,-250)
t.fillcolor('black')
t.begin_fill()
t.pendown()
t.goto(500,-250)
t.left(90)
t.goto(500,250)
t.left(90)
t.goto(-500,250)
t.left(90)
t.goto(-500,-250)
t.end_fill()
t.fillcolor('green')
t.begin_fill()
t.goto(-370,0)
t.goto(-225,-250)
t.end_fill()
t.fillcolor('green')
t.begin_fill()
t.penup()
t.goto(-300,-125)
t.pendown()
t.goto(-220,0)
t.goto(-40,-250)
t.end_fill()
t.fillcolor('yellow')
t.begin_fill()
t.penup()
t.goto(420,220)
t.pendown()
t.circle(20)
t.end_fill()
house(312,-250)
house(380,-250)
t.penup()
t.goto(0,0)
t.pendown()
t.ht()

t.done()